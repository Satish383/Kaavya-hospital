                </div>
                <p class="mb-4">Treatment for hip fracture usually involves a combination of prompt surgical repair,
                    rehabilitation, and medication to manage pain and to prevent blood clots and infection.</p>
                <p class="mb-4">The type of surgery generally depends on where and how severe the fracture is, whether
                    the broken bones aren't properly aligned (displaced), and your age and underlying health conditions.
                </p>
                <p class="mb-4">Metal screws are inserted into the bone to hold it together while the fracture heals.
                    Sometimes screws are attached to a metal plate that runs down the thighbone (femur).</p>
                <p class="mb-4">A health care provider can often diagnose a hip fracture based on symptoms and the
                    abnormal position of the hip and leg. An X-ray usually will confirm the fracture and show where the
                    fracture is.</p>
                <p class="mb-4">If your X-ray doesn't show a fracture but you still have hip pain, your provider might
                    order an MRI or bone scan to look for a hairline fracture.</p>
