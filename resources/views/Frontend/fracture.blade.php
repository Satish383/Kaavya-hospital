                </div>
                <p class="mb-4">External fixation involves placing metal pins or screws into the bone on either side
                    of the fracture and then connecting these pins with an external frame. This method is often used for
                    complex or open fractures when there is a risk of infection, swelling, or when internal fixation
                    isn't feasible.</p>
                <p class="mb-4">External fixation is a process for fracture fixation by which pins or wires are
                    inserted into bone percutaneously and held together via an external scaffold. Initially described by
                    Malgaigne in 1853, external fixation was proposed as an alternative to immobilization in plaster
                    cast, traction, or internal fixation.</p>
                <p class="mb-4">External fixator types divide into several different subcategories, including
                    uniplanar, multiplanar, unilateral, bilateral, and circular fixators. By adding pins in different
                    planes (i.e., placed perpendicular to each other), one can create a multiplanar construct. Uniplanar
                    fixation devices are fast and easy to apply but are not as sturdy as multiplanar fixation. Bilateral
                    frames are created when the pins are on both sides of the bone and can also add additional
                    stability.</p>
