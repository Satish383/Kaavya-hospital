                <p class="mb-4">The knee is made up of the lower end of the thighbone (femur), the upper end of the
                    shinbone (tibia), and the kneecap (patella). The ends of these three bones are covered with
                    articular cartilage, a smooth substance that protects the bones and enables them to move easily
                    within the joint.</p>
                <p class="mb-4">All remaining surfaces of the knee are covered by a thin lining called the synovial
                    membrane. This membrane releases a fluid that lubricates the cartilage, reducing friction to nearly
                    zero in a healthy knee.</p>
                <p class="mb-4">The menisci are located between the femur and tibia. These C-shaped wedges act as
                    shock absorbers that cushion the joint.</span></p>
                <p class="mb-4">Large ligaments hold the femur and tibia together and provide stability. The long
                    thigh muscles give the knee strength.</span></p>
